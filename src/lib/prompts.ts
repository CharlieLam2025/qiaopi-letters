// ────────────────────────────────────────────────
// 拼接给 LLM 的 messages
// ────────────────────────────────────────────────

export interface ComposeInput {
  to: string;
  from: string;
  destination: string;
  body: string; // 用户已经写的草稿，可能为空
  tone: LetterTone;
  theme: LetterTheme;
}

export function buildComposeMessages(
  input: ComposeInput
): Array<{ role: "system" | "user"; content: string }> {
  // system prompt（classical 拼上 few-shot）
  let system: string;
  if (input.tone === "classical") {
    const bucket = pickSampleBucket(input.to);
    const samples = SAMPLES_CLASSICAL[bucket] ?? SAMPLES_CLASSICAL.通用;
    // 取前 2 条作 few-shot（控制 token，约 200-400 tokens）
    const fewShot = samples
      .slice(0, 2)
      .map(
        (s, i) =>
          `【真实侨批文体范例 ${i + 1}】（${s.toLabel}）\n${s.text}`
      )
      .join("\n\n");

    system =
      CLASSICAL_RULES +
      "\n\n────────────────────────\n" +
      "下面是当时真实侨批的文体范例，请模仿这种语气、节奏、用词、行长。\n" +
      "不要照抄具体内容，只学语感。\n" +
      "────────────────────────\n\n" +
      fewShot;
  } else if (input.tone === "gentle") {
    system = GENTLE;
  } else if (input.tone === "restrained") {
    system = RESTRAINED;
  } else {
    system = MODERN;
  }

  const hasDraft = (input.body || "").trim().length >= 20;
  const userInstruction = hasDraft
    ? `请按上述语气改写下面这段话，保留它原本想说的意思，但调整为对应年代和笔触的"信"。`
    : `用户暂时没写正文，请你按上述语气替他/她拟一封短信。`;

  const userMsg = `【收信人】${input.to || "（未填）"}
【寄出地】${input.from || "（未填）"}
【寄往】${input.destination || "（未填）"}
【主题】${input.theme}
${hasDraft ? `【用户草稿】\n${input.body.trim()}\n` : "【用户草稿】（空）"}

${userInstruction}
请只返回信件正文文字本身，第一行是称呼，结尾不要署名。`;

  return [
    { role: "system", content: system },
    { role: "user", content: userMsg },
  ];
}

