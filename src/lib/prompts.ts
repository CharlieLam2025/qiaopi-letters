// 把用户的 "to" 输入映射到 sample bucket key
function pickSampleBucket(to: string): keyof typeof SAMPLES_CLASSICAL {
  const s = (to || "").trim();
  if (/(阿母|母亲|妈妈|妈|娘)/.test(s)) return "母亲";
  if (/(阿爸|父亲|爸爸|爸|爹|父)/.test(s)) return "父亲";
  if (/(妻|内子|爱人|夫人|秀英|阿娇)/.test(s)) return "妻子";
  if (/(兄|哥|大哥|二哥)/.test(s)) return "兄长";
  if (/(弟|小弟|三弟|四弟)/.test(s)) return "弟";
  if (/(阿嬷|嬷|奶奶|外婆|祖母|婆婆)/.test(s)) return "祖母";
  return "通用";
}

